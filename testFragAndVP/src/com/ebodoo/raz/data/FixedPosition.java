	public LayoutParams islandLocation(int index, int island, float scaleQPW, float scaleQPH, int xoffset, int yoffset, float suoxiao) {
		int islandPosition[][] = null;
		if(island == 0) {
			islandPosition = FixedPosition.island01_position;
		} else if(island == 1){
			islandPosition = FixedPosition.island02_position;
		} else if(island == 2){
			islandPosition = FixedPosition.island03_position;
		} else if(island == 3) {
			islandPosition = FixedPosition.island04_position;
		} else {
			islandPosition = FixedPosition.islandEnd_position;
		}
		if(index >= islandPosition.length) return null;
		RelativeLayout.LayoutParams params = LayoutParameters.setViewPositionParams(
				islandPosition[index][0],
				islandPosition[index][1],
				islandPosition[index][2],
				islandPosition[index][3], scaleQPW, scaleQPH,
				xoffset, yoffset, suoxiao);
		return params;
	}
	
	
